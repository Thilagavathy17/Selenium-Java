package strings;

import java.util.Arrays;

public class MutableVSImmutable {

	public static void main(String[] args) {
		
		//mutable
		int a[]= {20,40,10,50,30};
		System.out.println(Arrays.toString(a));
		Arrays.sort(a);
		System.out.println(Arrays.toString(a));
		
		//immutable

	}

}
